class for2 {
    public static void main(String[] args) {
        // Example 2: Iterating Over an Array
        // Print elements of an array
        int[] numbers = {10, 20, 30, 40, 50};

        for (int i = 0; i < numbers.length; i++) {
            System.out.println("Element at index " + i + ": " + numbers[i]);
        }
    }
}